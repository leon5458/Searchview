package com.hly.search_view;

import android.graphics.Bitmap;


public class iconInformation {
    private Bitmap icon;
    private String name;

    public Bitmap getIcon()
    {
        return icon;
    }

    public void setIcon(Bitmap icon)
    {
        this.icon = icon;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }
}
